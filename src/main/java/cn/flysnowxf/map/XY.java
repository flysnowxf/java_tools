package cn.flysnowxf.map;

public class XY {
	private int error = -1;
	private String x;
	private String y;
	
	public XY() {
		
	}

	public int getError() {
		return error;
	}

	public void setError(int error) {
		this.error = error;
	}

	public String getX() {
		return x;
	}

	public void setX(String x) {
		this.x = x;
	}

	public String getY() {
		return y;
	}

	public void setY(String y) {
		this.y = y;
	}
}
